a = int(input('')) # В первой строке попросить пользователя ввести целое число.
print(f'Следующее за числом {a} число: {a+1}')  # Во второй строке выводим следующее целое число.
print(f'Для числа {a} предыдущее число: {a-1}') # В третьей строке выводим предыдущее целое число.
# 35
# Следующее за числом 35 число: 36
# Для числа 35 предыдущее число: 34